            <h3 class="card-title">Bastone lungo (Gún)</h3>
            <p class="card-text">Il bastone è inserito nella categoria delle armi a lungo raggio. Le sue tecniche base
              comprende il fendente (pῙ), colpo con la parte posteriore (gài), oscillazione (lún), infilzare
              (chuō), spazzare (săo), ruotare (wŭhuā) ecc.<br><br>
              Nomi delle parti:<br>
              sezione posteriore<br>
              sezione centrale<br>
              sezione anteriore<br>
              base<br>
              corpo dell’asta<br>
              punta<br>
            </p>
            <h3 class="card-title">Modi di impugnare</h3>
            <p class="card-text">1. Presa avanti: afferrare la sezione posteriore con entrambe le mani, con parte dell’asta
              tra pollice e indice rivolti verso la punta;<br>
              2. Presa lenta: afferrare il bastone tra pollice e indice e distendere le altre tre dita. Usare
              quando è richiesta maggiore flessibilità del polso;<br>
              3. Presa Duishou: afferrare il bastone con entrambe le mani con parte dell’asta tra pollice
              e indice rivolti tra di loro.<br>
              La lunghezza del bastone deve essere maggiore dell’altezza di chi lo usa.
            </p>
            <h3 class="card-title">Spada dritta (Jian)</h3>
            <p class="card-text">La spada è un’arma convenzionale a corto raggio con una lama stretta e affilata a doppio
              taglio ed estremità appuntita. <br>Presenta il maggior numero di tecniche fra le diciotto armi
              più frequentemente usate, incluso fendente (liáo), perforare verso l’alto (tiăo),elevare (tί),
              appoggiare (tuō), agganciare (guà), tirare all’indietro (dài), spingere (cì), infilzare (diăn),
              infilzare in alto (bēng), frustare (jῙ), bloccare (gé lán) rotazione orizzontale (yūn) ecc.
            </p>
            <h3 class="card-title">Nomi delle parti</h3>
            <p class="card-text">
              Sezione debole<br>
