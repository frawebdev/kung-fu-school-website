            </p>
            <h3 class="card-title">Modi di impugnare</h3>
            <p class="card-text">1. Presa della mano destra: avvolgere l’elsa con la mano destra, pollice e indice stretti e
              attaccati alla guardia, le altre dita più morbide. Variare la pressione sull’impugnatura per
              portare l’arma alla stessa altezza del braccio in posizione di spinta orizzontale.<br>
              2. Presa della mano sinistra: posizionare l’elsa tra l’indice e il medio con la punta dell’arma
              rivolta in alto. Stringere la guardia con le dita e lateralmente col pollice. Girare il braccio
              verso l’esterno con lo spigolo della sciabola che tocca il lato interno dell’avambraccio e la
              lama più avanti e parallela al lato sinistro del corpo.<br>
              La lunghezza della sciabola, nella posizione di prsa siistra, deve arrivare almeno all’apice
              dell’orecchio di chi la usa.
              ecc.
            </p>
            <h3 class="card-title">Lancia (Qiang)</h3>
            <p class="card-text">La lancia è un’arma a lungo raggio. Tra le varie tecniche di base abbiamo la parata (lán.
              ná) e infilzare (zhā). Le tecniche di routine con la lancia sono costruite con quelle base
              combinate con rotazione (wŭhuā), fendente verso il basso (pῙ), infilzare verso l’alto (bēng),
              scivolare (chuān), girare (chán) e vari passi del corpo.
