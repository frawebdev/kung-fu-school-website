              ma nello stesso tempo si sono snaturate trasformandosi in semplici sport. Lo spirito delle
              arti marziali è invece completamente diverso da quello “sportivo”.<br> In Italia e in Occidente,
              in genere, si enfatizza troppo il concetto di arte marziale come abilità di combattimento e ci
              si allena per tirare calci all’avversario, per affermare il proprio ego, per sopraffare con la
              propria forza. Ciò non appartiene alle arti marziali cinesi. L’allenamento serve per
              migliorarsi continuamente e non per essere migliore di chiunque altro.<br>
              Il Kung Fu originale, tradizionale, quello vero, è prima di tutto un metodo per fortificare la
              mente, incrementare la salute e sensibilizzare il cuore.<br>
              Il Kung Fu è il termine con cui sono maggiormente note in Occidente le arti marziali
              tradizionali cinesi. In realtà, da secoli in Cina, esistono termini molto più precisi per
              definirle, quali ad esempio Wushu o semplicemente Wu.<br>
              Le arti marziali cinesi sono state codificate nei secoli seguendo le leggi della natura, della
              fisica, secondo una precisa conoscenza del corpo e dello spirito umani. I vari stili
              rappresentano metodi di combattimento di notevole efficacia che, tuttavia, si rivelano allo
              studente solo dopo un periodo piuttosto lungo, variabile secondo le attitudini individuali, di
              assidua pratica e dedizione.<br>
              L&#39;allenamento può avvenire o a mani nude o con l&#39;uso di armi che tradizionalmente sono
              diciotto, fra le quali vari tipi di bastoni, spada, sciabola, lancia, alabarda, catene, aste,
              mazze, pugnali, ecc.
              Le posizioni fondamentali e i principi sono gli stessi, ma la pratica comprende in più il
              metodo di maneggiare l&#39;arma, che è considerata come un prolungamento del corpo
              stesso fino a quando uomo e arma diventano una cosa sola.
              Si dedica molto tempo all&#39;allenamento delle forme e delle tecniche di base finché esse
              non divengono parte delle reazioni inconsce e istintive dell&#39;allievo. La sensazione di
              benessere che si prova dopo un corretto allenamento di Kung Fu si estende anche alle
              singole azioni della vita quotidiana. L’insegnamento si pone come ideale quello della
              formazione di un individuo completo, in armonia con se stesso e con l&#39;ambiente, in grado
              di vivere al massimo delle potenzialità umane.<br>
              Le arti marziali hanno visto i loro albori in Cina nella regione settentrionale di
              Henan sui monti Songshan, dove si trovava l&#39;ormai famosissimo Monastero Shaolin,
              ricostruito in epoca più recente. È la più antica delle arti marziali nata circa cinquemila anni
