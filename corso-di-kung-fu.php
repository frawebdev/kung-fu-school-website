            <h3 class="card-title">Modi di impugnare</h3>
            <p class="card-text">1. Presa inversa (con la mano sinistra): posizionare l’elsa sul palmo della mano con la
              spada rivolta verso l’alto e l’indice disteso lungo l’impugnatura.<br> Avvolgere l’elsa con il
              pollice e le altre dita. Avvicinare il braccio al corpo con il dorso della mano rivolto in avanti
              e la cresta centrale che tocca la parte posteriore dell’avambraccio.<br>
              2. Presa avanti (con la mano destra): Mano vicina alla guardia, avvolgere le dita attorno
              all’impugnatura con il margine laterale rivolto in alto.<br>
              3. Dita a spada: indice e dito medio distesi e uniti. Chiudere le restanti dita con il pollice
              che copre l’anulare.<br>
              La lunghezza della spada deve essere misurata consideranto che la punta deve essere, in
              presa inversa, più alta dell’orecchio di chi la usa.
            </p>
            <h3 class="card-title">Sciabola (Dao)</h3>
            <p class="card-text">La sciabola è un’arma corta con un solo lato affilato.<br> Tra i vari movimenti menzioniamo
              quelli attorno alla testa, il taglio (pῙ), fendere (kăn), agganciare (guà), taglio verso l’altoe
              (liáo), trafiggere (zhā), infilzare (diăn), taglio orizzontalmente (yún), infilzare in alto (bēng)
              ecc.
            </p>
