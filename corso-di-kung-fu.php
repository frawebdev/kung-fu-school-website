            <h3 class="card-title">Power training e Fa-Li</h3>
              <p class="card-text">Il Power training (o potenziamento muscolare) è un corso integrativo per i praticanti di
                Kung Fu o di Tai Chi. Ha come obiettivo quello di migliorare la forza, il tono muscolare e la
                resistenza aerobica.
                FA-LI si può tradurre come emissione di forza o forza esplosiva. L’insegnamento del Fa-Li
                normalmente inizia con degli esercizi preliminari dove l’attenzione è rivolta principalmente
                al rilassamento in modo da prevenire eccessiva tensione e rigidità.
                La chiave per arrivare al Fa-Li è, infatti, la coordinazione integrata di tensione e
                rilassamento, entrambi molto importanti.
                Normalmente è più facile trattenere che rilasciare, si tende più a frenare che liberare i
                gesti. Dal momento quindi che la tendenza delle persone è di produrre un’eccessiva
                tensione, e anche mal distribuita, è necessario insistere sull’importanza del rilasciamento
                muscolare e sul mantenimento di un atteggiamento rilassato ma vigile.</p>
