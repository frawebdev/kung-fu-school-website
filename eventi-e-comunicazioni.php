                <h3 class="card-title">biografia Shibo</h3>
                <p>
                Shi Xing Hong, il cui vero nome prima di divenire monaco Shaolin era De Qing, nasce il 27
                marzo 1974 in una piccola città di montagna di Tiantai, di circa diecimila abitanti, nella
                provincia di Zhejing. Frequenta per cinque anni una regolare scuola prima di essere
                “consegnato” dal padre al Tempio Shaolin, per cercare di metterlo in riga, visto il suo
                carattere aggressivo e litigioso.
                Suo maestro divenne Shi De Gong. A quel tempo, intorno al 1984, tutti i monaci vivevano
                e si allenavano nel Tempio. De Qing viveva, insieme a una ventina di altri bambini, in uno
                degli edifici del padiglione più piccoli all&#39;interno del Tempio e raramente aveva contatti con
                altri bambini che vivevano in altri piccoli padiglioni. Ogni gruppo, organizzato in base al
                livello di abilità, aveva il proprio maestro monaco per addestrarli e supervisionare il loro
                comportamento. I suoi anni dell&#39;adolescenza trascorsero quindi esclusivamente con il suo
                maestro e gli altri studenti del suo gruppo divenuti i suoi &quot;fratelli e sorelle di gong fu&quot;.
                La sua vita al Tempio era abbastanza irreggimentata. Il suo maestro controllava tutto e,
                per diversi anni non gli fu permesso di lasciare il Tempio per incontrare la famiglia
                d’origine.
                All&#39;età di diciotto anni gli fu offerta l&#39;opportunità di diventare monaco. Aveva deciso che
                avrebbe trascorso la sua vita imparando il buddismo, addestrando e insegnando il gong fu.
                Come monaco approfondì lo studio del buddismo e diminuì, sebbene di poco,
                l&#39;addestramento marziale. Tuttavia è da quel momento che i monaci iniziano a imparare i
                veri segreti del gong fu.
                Dal 1991 al 1995, DeQing (ora Shi Xing Hong) iniziò a viaggiare a livello internazionale
                con altri monaci per esibirsi con il team degli spettacoli e insegnare il gong fu. Inizialmente
                venne in Italia nel 1991 con altri quattro monaci per allenarsi ed esibirsi. Successivamente,
                andò con altri gruppi di monaci Shaolin in Thailandia, Malesia, Canada, Giappone e
                Taiwan. Nel 1995, per il 1500° anniversario del Tempio di Shaolin, fu organizzato un
                grande tour in Europa durante il quale furono diffusi CD e video che aumentarono
                notevolmente la fama dei monaci Shaolin nel vecchio continente.
                Nel 1997 trascorse sei mesi in Spagna insegnando con un suo allievo spagnolo,
                precedentemente addestrato a Shaolin. Nel 1998 decise di aprire la sua scuola di gong fu
                a circa quindici chilometri dal villaggio di Shaolin la cui gestione economica però si rivelò
                non semplice per una persona la cui unica formazione era stata nelle arti marziali e nel
                buddismo. Gli studenti della scuola pagavano quello che potevano e Shi Xin Hong non
                allontanava mai nessuno visto l’effetto incredibile che la pratica di Gong Fu aveva sugli
                allievi. Pertanto, per far quadrare i conti, decide di insegnare agli stranieri e continuare la
                programmazione di spettacoli nei vari Paesi. Tutti i suoi guadagni erano destinati a far
                funzionare la sua scuola. Egli sentiva fortemente la sua vocazione d’insegnare e di
                continuare la tradizione del Kung Fu come gli era stato a sua volta insegnato.
                Nel contempo vince anche cinque tornei nazionali cinesi ma il suo unico interesse è ora
                l’insegnamento.
                Avvia scuole di Shaolin Kung Fu negli Stati Uniti. Ritorna in Europa e in Ungheria ne apre
                un’altra, dove insegna anche alla polizia locale l’arte del Kung Fu.
                Nel 2005 è ideatore, promotore, fondatore e presidente della Federazione Internazionale
                Chan Wu.
                Tiene costantemente seminari intensivi in vari Paesi sparsi nel mondo. Anche l’Italia è
                sempre presente nel suo cuore e vi ritorna molto volentieri compatibilmente con il suo fitto
                taccuino di appuntamenti e impegni.
                Shi Xin Hong è, tra l’altro, il maestro del nostro maestro Liu Peng, come si vede da questa
                foto.
