          <h3 class="card-header" id="draghi-section">Draghi</h3>
              <div class="card-body">
                <p class="card-text">In molte culture di tutto il mondo, il drago è stato, per migliaia di anni, un
                  simbolo ed è ancora oggi è molto popolare in Cina.<br>
                  Per gli antichi Cinesi, il drago aveva attributi divini di potere: poteva
                  trasformarsi e rendersi anche invisibile, capace di chiamare il vento e la
                  pioggia, di controllare le forze naturali e li aiutava anche in battaglia.
                  Sin dall’antichità è uno dei simboli più usati come motivo decorativo nell’arte,
                  negli edifici, nell’arredamento e nelle feste popolari. Gli imperatori cinesi, già
                  a partire dalla dinastia degli Han (206 a.C. – 220 d.C.), hanno scelto il drago
                  come simbolo imperiale.<br><br>
                  I testi antichi ne descrivono quattro tipi:<br>
                  “draghi del cielo” (tian long), che simboleggiano la potenza rigenerativa del
                  paradiso e l’imperatore.<br>
                  “draghi-spirito” (shen long), che controllano le piogge, il vento e le nuvole.
                  “draghi della terra” (di long), che portano la primavera e controllano i corsi
                  d’acqua.<br>
                  “guardiani del tesoro” (fu can long), considerati un po’ come i nostri angeli
                  custodi.<br><br>
                  Ai Cinesi attuali piace il drago come portafortuna e simboleggia una crescita
                  equilibrata dello spirito, portatore di cambiamenti, associato al potere della
                  trasformazione, alla ricerca di saggezza, felicità e immortalità.
                </p>
                <h3 class="card-title">La danza del drago</h3>
                <p class="card-text">In Cina durante i festeggiamenti del capodanno cinese, così come in
                  occasione di matrimoni, gruppi di figuranti formano un lungo serpentone con
                  la testa a forma di drago. In queste occasioni la danza è di auspicio alla
                  prosperità e alla fertilità.<br>
                  La danza del drago ebbe inizio durante la dinastia Han e la sua popolarità,
                  dopo migliaia di anni, non mostra alcun segno di declino. Dapprima essa era
                  rappresentata durante le cerimonie sacrificali agli antenati. La danza
                  intendeva rappacificare il re dei draghi e far piovere sulla terra divenuta arida
                  a causa della siccità. La danza divenne uno spettacolo festoso durante le
                  dinastie Tang e Song.<br>
                  La leggenda riguardante la danza del drago racconta che il re dei draghi
                  soffrisse di mal di schiena e le medicine non lo guarivano.<br> Si travestì quindi
                  da vecchio e andò sulla Terra per cercare un dottore. <br>Egli gli auscultò il polso
                  e, sorpreso, gli chiese: “Sei un essere umano?” Vedendo che non poteva più
                  nascondere il suo travestimento, il re dei draghi gli disse la verità.<br> Il dottore gli
                  chiese di ritornare alle sue vere sembianze e gli trovò, sotto una squama del
                  girovita, un millepiedi e lo rimosse.<br> Tramite la coppettazione gli estrasse il
                  veleno, gli curò la ferita e così il re dragone guarì presto e, per mostrargli la
                  sua gratitudine, rivelò al medico il suo segreto: “Se costruirete dei draghi
                  come me e giocherete con loro, avrete un buon tempo per il grano e per i
                  raccolti”. <br>Quando la notizia si sparse, la gente preparò dei draghi e danzò con
                  essi per ingraziarsi il re dragone e far piovere durante i periodi di siccità. <br>Più
                  tardi, la tradizione della danza si sviluppò facendo danzare il drago verde in
                  primavera, quello rosso in estate, il drago bianco in autunno e nero in
                  inverno.
                </p>
                <h3 class="card-title">Origine della danza del Leone</h3>
                <p class="card-text">La danza del leone è confusa spesso con quella del drago, tuttavia una
                  differenza fondamentale è che il corpo del leone è normalmente mosso da
                  due danzatori, mentre il drago ne richiede molto.<br>
                  Le danze cinesi del leone si possono dividere, in generale, nello stile del Nord
                  e in quello del Sud.<br>
                  I leoni del Nord hanno, per lo più, una criniera arruffata gialla e arancione con
                  un fiocco rosso o verde sopra la testa, per indicare maschio o femmina.<br> Il
                  leone del Sud presenta una più ampia gamma di colori, con una testa
                  caratterizzata da grandi occhi, uno specchio sulla fronte e un corno al centro.<br>
                  Ci sono molte storie che narrano l’origine di questa danza. La danza del
                  leone è eseguita ora durante festival ed eventi importanti, non soltanto come
                  spettacolo, ma anche come simbolo della cultura e tradizione cinese per
                  augurare alla comunità felicità, buona fortuna e benedizioni.
                </p>
