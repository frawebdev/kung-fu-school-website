            <h3 class="card-header" id="tè-section">La cultura del tè in Cina</h3>
            <div class="card-body">
              <p class="card-text">La Cina ha una lunga tradizione per quanto riguarda la bevanda del tè. Infatti
                lo consumano da ben quattromila anni. Lu Yu (733-804 d.C.), conosciuto
                come il “Saggio del Tè”, attribuì la sua scoperta a un leggendario imperatore
                cinese, Shen Nong nel 2737 a.C. Fu in quel tempo che bere il tè divenne
                molto diffuso e la parola “Cha Dao” cominciò a essere usata riferita all’atto di
                gustarlo.<br>
                Durante la dinastia Song, fra gli ufficiali governativi e gli intellettuali, bere il tè
                era considerato di moda ed erano organizzate gare di abilità riguardanti la
                sua preparazione. Durante la dinastia Ming, il tè divenne una bevanda
                assaporata da tutti poiché le sue proprietà pratiche furono maggiormente
                enfatizzate rispetto alle sue connotazioni sociali delle dinastie precedenti.
                Tradizionalmente, i cultori del tè erano considerati come una élite in quanto la
                pratica di bere il tè era espressione di rettitudine, istruzione e principi morali.
                Le proprietà specifiche scoperte dai medici e ricercatori cinesi furono,
                probabilmente, la ragione per il suo crescente apprezzamento.
                Durante la dinastia Tang, un monaco giapponese si innamorò di questa
                bevanda e la esportò in Giappone.<br>
                In Cina, infatti, il tè non è solo una bevanda ma anche un ingrediente
                fondamentale della Medicina Tradizionale Cinese. La Materia Medica di Shen
                Nong annota che egli usava il tè come rimedio. Hua Tuo, il famoso dottore
                cinese della dinastia degli Han dell’Est, (25-220 d.C.), è spesso citato per la
                sua affermazione: “bere costantemente tè amaro ti fa sentire meglio”. La
                capacità del tè di incrementare le funzioni cerebrali può spiegare perché il
                berlo era così diffuso fra i monaci buddisti nelle dinastie Wei e Jin e veniva
                consumato per il suo effetto rinfrescante e per il fatto di favorire la
                meditazione.<br>
                Il tè ha anche altre proprietà, come quella di estinguere la sete, eliminare il
                grasso, limitare la dispepsia e migliorare la vista.<br>
                Nel 1610 arrivò in Europa attraverso Macao, a bordo di una nave mercantile
                olandese, diventando così una bevanda internazionale.<br>
                Oggi in Cina il tè rappresenta l’elemento culturale fondamentale. Esso ha
                avuto una grande influenza sullo sviluppo della cultura cinese.<br> Il tè è spesso
                associato alla letteratura, alle arti e alla filosofia, ma anche al taoismo,
                buddismo e confucianesimo. Anche la più umile abitazione possiede un set di
                strumenti per la preparazione del tè come simbolo di accoglienza e rispetto
                verso i visitatori per sancire l’importanza dell’evento.<br>
                La cultura del tè si riferisce al modo in cui esso è prodotto, preparato e
                consumato e come le persone interagiscono con questa bevanda. Esso
                riflette le caratteristiche etniche, estetiche ed etiche delle culture nelle
                differenti regioni e periodi.<br> Le implicazioni culturali del tè si possono trovare
                principalmente nelle cerimonie, come ad esempio, matrimoni, funerali e altri
                avvenimenti della vita quotidiana.<br> Bere tè, come pure usarlo nelle differenti
                interazioni sociali, indica uno stile di vita.<br>
                Le persone spesso lo sorseggiano nel tempo libero per avere una vita
                mondana. Eleganza, semplicità, frugalità e intellettualismo sono le
                caratteristiche più specifiche del tè, per incrementare la salute, per affermare
                la propria individualità e reputazione e come modo di essere.
               </p>
               <h3 class="card-title">Tipi di tè</h3>
               <p class="card-text">
                I tè sono classificati tenendo conto diversi elementi, zona di produzione, il
                mercato di destinazione, le dimensioni delle foglie, lavorazione, ecc…<br>
                Tè verde Lucha (ossidato)<br>
                Tè nero Heicha (completamente ossidato)<br>
                Tè “semi-fermentato” Qingcha (Oolong), caratterizzato da media ossidazione,
                e inoltre Huangcha (Tè giallo), Baicha (Tè bianco) e Hongcha (Tè rosso).
              </p>
              <h3 class="card-title">Preparazione</h3>
                <p class="card-text">
                  Ci sono diversi modi per preparare il tè. Quello più informale, usato in casa o
                  nei ristoranti, è la semplice aggiunta di foglie in una pentola con acqua calda.
                  Altri due metodi principali sono il metodo Chaou e il metodo Kung Fu Cha.<br>
                  Metodo Chaou<br>
                  È adottato nelle occasioni formali utilizzando i tè più delicati, un coperchio,
                  una tazza/ciotola e un piattino, solitamente in porcellana.<br>
                  Metodo Kung Fu Cha<br>
                  Questo procedimento è nato e si è sviluppato nelle province cinesi del Fujian
                  e del Guangdong. Il nome significa “tè preparato con impegno, cura e
                  attenzione” e si utilizza soprattutto il tè Oolong. L’impegno riservato nella sua
                  preparazione è il medesimo che s’impiega nella preparazione atletica delle
                  arti marziali cinesi. Molta attenzione è anche riservata alla scelta dei
                  materiali, tutti naturali; alcuni maestri sostengono che l’acqua non deve mai
                  toccare il metallo che rilascia un sapore sgradevole. Altri strumenti utilizzati
                  sono:<br><br>
                  
                  - teiera tradizionale, detta Yixing, caratterizzata da una certa porosità, in
                  modo da assorbire gli aromi del tè che vi sono infusi<br>
                  - pinze per manipolare le tazzine bollenti<br>
                  - un cucchiaio ottenuto da una sezione di canna di bamboo per misurare la
                  quantità di tè<br>
                  - un vassoio con doppio fondo cavo per raccogliere l’acqua in eccesso
                  durante la preparazione<br>
                  - un bollitore<br>
                  - altri strumenti affusolati per raccogliere le foglie dall’interno della teiera.
              </p>
