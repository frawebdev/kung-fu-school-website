              <h3 class="card-header" id="monastero-section">Storia del monastero Shaolin</h3>
                <div class="card-body">
                  <p class="card-text">Shaolin è il nome del famoso monastero buddhista in cui per millenni si sono
                    praticate le arti marziali. Attualmente è divenuto un termine piuttosto generico
                    che indica sia l&#39;insieme di tutti gli stili del Kung Fu, sia l&#39;arte originaria
                    insegnata nel tempio Shaolin dalla quale derivano anche, direttamente o
                    indirettamente, quasi tutte le altre arti marziali asiatiche.<br>
                    Il Monastero Shàolίn è il luogo di nascita del Buddismo cinese Chán (Zen in
                    giapponese). Grazie alla pluricentenaria pratica di Kung Fu dei monaci
                    Shàolίn, questo luogo è considerato “sacro” dai praticanti di quest’arte
                    marziale.<br>
                    Esso originariamente fu fondato nel 19° anno dell’era Taihe Dinastia Wei del
                    Nord (495 d.C.). Fu costruito nella parte nord del Shaoshi, la cima
                    occidentale della montagna Song, a 13 km Nord-Ovest della contea Deng
                    Feng, incastonato fra montagne e foreste.<br> Il tempio è conosciuto come la
                    culla del Buddismo cinese Chán (Zen) come pure per la Scuola di Arti
                    Marziali Shàolίn. È un luogo che attira persone da tutto il mondo.<br>
                    Durante le Dinastie del Nord e del Sud, un monaco indiano, Batuo, arrivò in
                    Cina per predicare il Buddismo. Per volere dell’imperatore Wei Xiao Wen
                    (471-500 d.C.) fu costruito un tempio nella foresta della montagna Shaoshi. Il
                    carattere cinese per la parola “foresta” è “lin”, da qui il nome Shàolίn. Batuo
                    predicava il Buddismo Theravada presso il Tempio di Shàolίn e ne fu anche il
                    primo abate.<br> Fra i suoi numerosi discepoli Seng Chou, un Maestro Zen e Hui
                    Guang furono i più conosciuti per il loro talento, sia nel Buddismo sia nelle arti
                    marziali.<br>
                    Nell’anno 527 d.C. il monaco indiano Bodhidharma, 28° generazione di
                    discepoli di Sakyamuni, giunse nelle montagne Song. Si dice che egli
                    trascorse nove anni in meditazione, col viso rivolto verso il muro di una
                    caverna situata sotto il picco Wŭru nella catena dei monti Song, al di sopra
                    del Monastero.<br> Successivamente giunse al Tempio per predicare il
                    Buddismo Mahayana. A lui è attribuito il fatto di aver portato il Buddismo
                    Chán in Cina. Quando egli trasmise a Hui Ke le insegne del Patriarcato, il
                    Buddismo Chán prevalse nel Monastero Shàolίn. Bodhidharma è
                    considerato come il Primo Patriarca del Buddhismo Chán cinese e il Tempio
                    Shàolίn come la sede del Buddismo Chán.<br>
                    Durante la Dinastia Sui (581-618 d.C.), l’imperatore Wen incoraggiò la
                    diffusione del Buddismo e fece donazioni per la costruzione e il
                    mantenimento del Monastero Shàolίn. In tutto il periodo del suo regno
                    accordò al Tempio 100 qing di terreno (equivalenti a 667 ettari) per ampliare
                    le abitazioni dei monaci.<br>
                    Durante la Dinastia Tang (618-947 d.C.), l’imperatore Li Shiming assegnò 40
                    qing di terre al Monastero e conferì il titolo di Capo Supremo dei Monaci a
                    Tan Zong per il suo aiuto nell’insediamento della Dinastia Tang. L’imperatore,
                    inoltre, diede il permesso al Tempio Shàolίn di mantenere un reparto di 500
                    monaci guerrieri. Fu in quel tempo che al Monastero Shàolίn fu coniato il
                    nome di “Primo Monastero sotto al Cielo”.<br>
                    Durante la Dinastia Song (960-1279 d.C.), sia il Monastero sia le arti marziali,
                    continuarono a svilupparsi. A quel tempo il numero dei monaci raggiunse le
                    duemila unità. La Dinastia Ming (1368-1644 d.C.) vide il primo sviluppo della
                    Scuola di arti marziali. Fra le migliaia di monaci emersero numerosi talenti
                    che compirono varie prodezze. Questo periodo è chiamato “La primavera e
                    l’autunno di Shàolίn”.<br>
                    L’attuale ingresso principale del Tempio fu costruito nel 13° anno del regno
                    dell’imperatore Yongzheng (1735 d.C.). Kangxi, il secondo imperatore Qing
                    dell’ultima dinastia (1644-1911 d.C.) scrisse l’iscrizione calligrafica che, ai
                    giorni nostri, sovrasta la porta principale del Tempio.</p>
